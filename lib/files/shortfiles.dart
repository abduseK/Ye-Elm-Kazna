'ነገር ግን የዑስማን ዘመን በመጣ ጊዜ ሰዎች በተበራከቱና ቤቶችም ለጁሙዓ '
'ተሰባስበው ሲሰግዱበት ከነበረው ብቸኛው መስጂድ (መስጂደ ነበዊይ) በራቁ '
