'የማይችሉ ከሆነ በራሳቸው ቋንቋ ተርጉሞ ኹጥባ ማድረግ ይቻላል። '
'ምክንያቱም ኹጥባ ዋናው አላማው መምከርና ማስተማር ስለሆነ ልክ '
'እንደማንኛውም ዳእዋ በማንኛውም ቋንቋ ንግግር አድርጎ አዳማጮቹ ከኹጥባው ተጠቃሚ መሆን ስላለባቸው ነው።',




'ቁርአን የወረደው ተገንዝበነውና  አስተንትነነው እንድንሰራበት እንጅ ልክ እንደ '
'አዛን ሰዎችን ወደ መስጂድ መጥሪያነት እንድንጠቀመው አይደለም። '
'ነብየም ሶለሏሁ አለይሂ ወሰለም ሱናቸውን አጥብቀን ይዘን እሳቸውን '
'ከመፃረርና በዲናቸው ላይ አዲስን ነገር ከመፍጠር እንድንቆጠብ አዘውናል።\n\n '

'በሀገራችን በበርካታ መስጂዶች እንደሚስተዋለው ጁሙአ ቀን በማይክራፎን ቁርአን '
'መክፈት በሸሪዓችን መሰረት የሌለውና ነብዩም ሶለሏሁ አለይሂ ወሰለም ያላዘዙት ፣ '
'ሰለፎችም የማያውቁት ቢድአ የሆነ ተግባር ነው። በዚህ መልኩ የጁሙአ ቀን ሁሉም '
'ሙስሊም በግሉ ቢቀራቸው የሚወደዱ የቁርአን ምእራፎችን እንጅ መስጂድ ላይ '
'ቁርአን መክፈት የተወደደ መሆኑን የሚያመለክት ሸሪዓዊ ማስረጃ የለም።\n\n'

'ሰዎችን ወደ መስጂድ ለመጥራት ታስቦበትም ከሆነ በጁሙአ አዛን '
'እንጅ በቁርአን መጥራት አልተደነገገም። የጁሙአ ቀን ስለሆነ ተብሎም ከሆነ '
'በዚህ መልኩ ጁሙአ ቀንን በሸሪዓችን ባልተደነገገው መልኩ መለየትም አይፈቀድም።',




'አንድ ሰው የጀናባም የጁሙዓንም ትጥበት ከተሰባሰበበት ለሁለቱም አንዴ ብቻ መታጠብ ይበቃዋል። '
'ባይሆን የጀናባንና የጁሙዓን ትጥበት እንደሚታጠብ ቀድሞ መነየት (ማሰብ) ይገባዋል። ለዚህም ነብዩ እንዲህ ብለዋል: \n'
'\"ስራዎች ሁሉ በኒያ (በማሰብ) ነው። ለእያንዳንዱ ግለሰብ የነየተው '
'(ያሰበው) አለው።\" ቡኻሪ (1) እና ሙስሊም (1907) ዘግበውታል።',



'በርግጥ ነብዩ - ﷺ - የጁሙዓ ቀን ሁለት ኹጥባዎችን እንደሚያደርጉና በመካከልም '
'ሚንበሩ ላይ ትንሽ አረፍ በማለት በሁለቱ ኹጥባዎች መካከል መለያ ያደርጉ እንደነበር '
'በሶሂህ ሀዲስ ተዘግቧል። ነገር ግን በዚች በኹጥባው መካከል ላይ ኹጥባ ለሚያደርገውም '
'ይሁን ለአዳማጩም የሚባል የሆነ ልዩ ዱዓ ወይም አዝካር አልተዘገበም። ባይሆን በርካታ '
'ኡለሞች እንደሚሉት በዚች ወቅት ላይ ሌሎችን እንዳይረብሹ ድምፅን ዝቅ አድርጎ ዱዓ ማድረግ የተወደደ ነው።',


'በርግጥም አጠቃላይ የሙስሊሞች ስምምነት እንደሚያመለክተው ከነብዩ ﷺ ጀምሮ በአቡበክርና '
