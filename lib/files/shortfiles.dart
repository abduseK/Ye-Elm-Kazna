'ሆነ በጀመዓ በአንድ ድምፅ አሚን ማለት እንዲሁም እጅን ከፍ ማድረግ አልተደነገገም። መሰረት የሌላቸው ተግባራት ናቸው።',



'በዚህ ዙሪያ ኡለሞች የአቋም ልዩነት ቢኖርም የተሻለውና በላጩ አቋም ግን '
'ኹጥባን በአረበኛ ማድረግ በላጭ ቢሆንም ኹጥባውን የሚያዳምጡት '
