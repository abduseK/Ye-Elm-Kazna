'በሌላም ጊዜ ከአህለሱና ናቸው ይላሉ ተብለው ሲጠየቁ እንዲህ ብለዋል:—'
'\" እኔ ከኢኽዋነል ሙስሊሚን አካሄድ የጠራሁ ነኝ። በዚህ ዙሪያ መልስ ሰጥቻለሁ። አካሄዳቸውንም በደንብ አብራርቻለሁ።\"\n'

'ሸይኽ ሷሊህ ሉሀይዳን ረሂመሁሏህ:— '
'\"ኢኽዋነል ሙስሊሚን በትክክለኛው የኢስላም አካሄድ ላይ አይደሉም።',



