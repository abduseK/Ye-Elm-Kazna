'3: የቁርአን አንቀፆችን ስሜታቸውንና ፍላጎታቸውን ባገናዘበ መልኩ ጠምዝዘው መተርጎማቸው ፣ '
'ከነዚህም መካከል ስለ ጂሀድ የሚያወሩ \"መውጣት\" የሚል ቃል ያለባቸውን አንቀፆች በሙሉ \"ለዳእዋ_መውጣት\" በሚል መተርጎማቸው ፣\n\n '

'4: የሚወጡባቸውን የዳዕዋ ወቅቶችን በቀናት ፣ በወራትና በአመታት ቁጥር መገደባቸውና '
'ለዚህም ገደባቸው ምንም ግኑኝነት የሌላቸው ስለ ሌላ ቁጥር የሚያወሩ የቁርአን አንቀፆችን ማስረጃ ማቅረባቸው ፣ \n\n'

'5: ዶዒፍ የሆኑና የተቀጠፋ ሀዲሶችን እንዲሁም መሰረተ_ቢስ የሆኑ እንቶፈንቶ '
'ወሬዎችን በዳዕዋቸው ላይ መጠቀማቸውና ከዚህ የጠሩ ዳእዋዎችን ደረቅ ዳእዋ ብለው ማለታቸው ፣\n\n '

