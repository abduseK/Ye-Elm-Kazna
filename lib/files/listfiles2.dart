'በተለይ ይህ ቀለበት እስካልወለቀ ድረስ ፍቅርን ያጠነክራል ልዩነት አይኖርም ተብሎ የሚታሰብ ከሆነ ' 
'ደግሞ ሽርክ ነውና የሚሆነው ወደ ሽርክ ከሚያመሩ መንገዶችም አንዱ ሊሆን ይችላልና የተከለከለ ይሆናል።',



'ቢድዓ አስከፊና ከሰው ወደ ሰው የሚተላለፍ የልብ በሽታ ከመሆኑ የተነሳ ኡለሞች '
'ሙብተዲዕ ጋር መቀማመጥን እና የሱና ኡስታዞች እያሉ ከእርሱ እውቀት መማርን ይከለክላሉ። '
