'ላይ ብቻ የተመረኮዘ ኢልም ይዘው ዳእዋ ያለ በቂ እውቀት ማድረጋቸው ፣ '
'ለዚህም አሏህ እንዲህ ይላል: \"በል : ይህች መንገዴ ናት ፣ ወደ አሏህ እጣራለሁ ፣ '
'እኔም እኔንም የተከተሉኝ በግልፅ ማስረጃ (ጥልቅ እውቀት) ላይ ነን ፣ ጥራትም ለአሏህ የተገባው ነው ፣ እኔም ከአጋሪዎች አይደለሁም።\" '
