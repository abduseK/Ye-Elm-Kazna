'ነህውና፣ ሶርፍ እና ተጅዊድ እንኳን ሰውዬው ሲያስተምር ወደ ቢድዓው '
'የማይጣራ እና ኪታቡ ላይ ያለውን ብቻ የሚያስተምር ከሆነ እና ይህ እውቀት '
'ከእርሱ ውጭ በአካባቢው ከሌላ ሰው የማይገኝ ከሆነ ብቻ እንጅ አይፈቀድም።',



'የግድ ካልሆነ በስተቀር ማለትም ለመታወቂያ ፣ ለፓስፖርት ፣ ለመንጃፈቃድ እና ለሌሎች '
