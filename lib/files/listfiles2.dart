'ኡለሞች ያላቸውን አቋም ግልፅ አድርገዋል። ያሉባቸውን የተለያዩ ነጥቦችን በማንሳት ከአህለሱና '
'ከወጡት ከጥመት ቡድኖች መሆናቸውንም ገልፀዋል። ከነዚህ ነጥቦች መካከል:— \n\n'

